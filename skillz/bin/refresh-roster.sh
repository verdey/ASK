#!/usr/bin/env bash
# refresh-roster.sh — scan ~/.claude/skills/*/SKILL.md, write roster.md
# Mirrors the kingdom's refresh-manifest.sh pattern, applied to skills.
# KISS: pure bash + python3 (stdlib only). No deps to install.

set -euo pipefail

SKILLS_DIR="${HOME}/.claude/skills"
ROSTER="${SKILLS_DIR}/skillz/roster.md"

python3 - <<'PY' "$SKILLS_DIR" "$ROSTER"
import os, sys, re, time
from datetime import datetime, timezone

skills_dir, roster_path = sys.argv[1], sys.argv[2]
now = time.time()
today_iso = datetime.now().strftime("%Y-%m-%d")

def parse_frontmatter(path):
    try:
        with open(path, "r", encoding="utf-8") as f:
            text = f.read()
    except Exception:
        return None
    m = re.match(r"^---\s*\n(.*?)\n---\s*\n", text, re.DOTALL)
    if not m:
        return None
    block = m.group(1)
    fm = {}
    # very small YAML-ish parser: key: value (single line, optional quotes)
    for line in block.splitlines():
        mm = re.match(r'^([A-Za-z_-]+):\s*(.*)$', line)
        if mm:
            k, v = mm.group(1), mm.group(2).strip()
            if (v.startswith('"') and v.endswith('"')) or (v.startswith("'") and v.endswith("'")):
                v = v[1:-1]
            fm[k] = v
    return fm

def status_for(skill_dir, mtime):
    # paused if any _PAUSE_*.md present anywhere in the skill dir
    for root, _, files in os.walk(skill_dir):
        for fn in files:
            if fn.startswith("_PAUSE_") and fn.endswith(".md"):
                return "⏸ paused"
    age_days = (now - mtime) / 86400
    if age_days >= 90:
        return "💤 dormant"
    return "🟢 active"

def relative_time(mtime):
    delta = now - mtime
    if delta < 86400:
        return "today"
    days = int(delta // 86400)
    if days == 1:
        return "yesterday"
    if days < 30:
        return f"{days}d ago"
    if days < 365:
        return f"{days // 30}mo ago"
    return f"{days // 365}y ago"

rows = []
for entry in sorted(os.listdir(skills_dir)):
    skill_dir = os.path.join(skills_dir, entry)
    if not os.path.isdir(skill_dir):
        continue
    if entry.startswith("_") or entry.startswith("."):
        continue
    skill_md = os.path.join(skill_dir, "SKILL.md")
    if not os.path.isfile(skill_md):
        continue
    fm = parse_frontmatter(skill_md) or {}
    if not fm.get("description"):
        # fall back to first non-empty line after any heading
        try:
            with open(skill_md) as f:
                for line in f:
                    s = line.strip()
                    if s and not s.startswith("#"):
                        fm["description"] = s
                        break
        except Exception:
            pass
    fm.setdefault("name", entry)
    fm.setdefault("description", "_(no description)_")
    mtime = os.path.getmtime(skill_md)
    rows.append({
        "name": fm.get("name", entry),
        "description": fm.get("description", "").replace("|", "\\|"),
        "status": status_for(skill_dir, mtime),
        "mtime": mtime,
        "rel": relative_time(mtime),
        "iso": datetime.fromtimestamp(mtime).strftime("%Y-%m-%d"),
        "source": "local",
    })

# sort newest-first
rows.sort(key=lambda r: r["mtime"], reverse=True)

lines = []
lines.append(f"# Council Roster — refreshed {today_iso}")
lines.append("")
lines.append(f"_Auto-generated by `bin/refresh-roster.sh`. {len(rows)} local skills found at `~/.claude/skills/`. Do not edit by hand — edit each skill's `SKILL.md` frontmatter and re-run._")
lines.append("")
lines.append("| Skill | Description | Status | Last touched | Source |")
lines.append("|-------|-------------|--------|--------------|--------|")
for r in rows:
    lines.append(f"| `/{r['name']}` | {r['description']} | {r['status']} | {r['rel']} ({r['iso']}) | {r['source']} |")
lines.append("")
lines.append("## Plugin skills (not auto-scanned)")
lines.append("")
lines.append("Plugin-namespaced skills live outside `~/.claude/skills/` and are surfaced by Claude Code's active skill list, not this scanner. Known active plugins as of last manual sync:")
lines.append("")
lines.append("- `commit-commands:*` — commit, commit-push-pr, clean_gone")
lines.append("- `anthropic-skills:*` — skill-creator, consolidate-memory, schedule, setup-cowork, pdf, docx, pptx, xlsx")
lines.append("- `frontend-design:frontend-design`")
lines.append("- `supabase:supabase`, `supabase:supabase-postgres-best-practices`")
lines.append("")
lines.append("When `/skillz` runs, it should also read the system's available-skills list from the current session and merge it into the roster view.")
lines.append("")

with open(roster_path, "w", encoding="utf-8") as f:
    f.write("\n".join(lines))

print(f"wrote {roster_path} ({len(rows)} skills)")
PY
